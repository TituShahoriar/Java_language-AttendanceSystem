class Teacher {
    String teacherID;
    String teacherName;
    String className;
    String sectionName;

    public Teacher(String teacherID, String teacherName, String className, String sectionName) {
        this.teacherID = teacherID;
        this.teacherName = teacherName;
        this.className = className;
        this.sectionName = sectionName;
    }
}